def add(a, b):
    print(f"Addition of {a} and {b} is: {a + b}")

def sub(a, b):
    print(f"Subtraction of {a} and {b} is: {a - b}")

def mul(a, b):
    print(f"Multiplication of {a} and {b} is: {a * b}")

def div(a, b):
    print(f"Division of {a} and {b} is: {a / b}")

def mod(a, b):
    print(f"Modulus of {a} and {b} is: {a % b}")


